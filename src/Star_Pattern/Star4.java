package Star_Pattern;

public class Star4
{
	//*
	//**
	//***
	//****

	public static void main(String[] args)
	{
	 int star=1;
		for(int a=1; a<=4; a++)
		{
			for(int b=1; b<=star; b++)
			{
				System.out.print("*");
			}
			System.out.println();
			star++;
		}
		
		
		
		
		
		
		
	}
}
